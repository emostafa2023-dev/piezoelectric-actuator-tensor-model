import numpy as np
import matplotlib.pyplot as plt
from mpl_toolkits.mplot3d.art3d import Poly3DCollection

# ==========================================================
# 1. ПАРАМЕТРЫ МАТЕРИАЛА PZT-5H (Тензор пьезомодулей)
# ==========================================================
# Значения в м/В (метры на Вольт)
d31 = -274e-12  # Поперечный коэффициент
d33 = 593e-12   # Продольный коэффициент
d15 = 741e-12   # Сдвиговый коэффициент

# ==========================================================
# 2. ГЕОМЕТРИЯ И УСЛОВИЯ (Система: 1-X, 2-Y, 3-Z)
# ==========================================================
L = 0.020    # Длина по оси X (20 мм)
W = 0.005    # Ширина по оси Y (5 мм)
H = 0.010    # Высота по оси Z (10 мм)

# Подаем напряжение
Uz = 500     # Напряжение по вертикали (вызывает растяжение/сжатие)
Ux = 300     # Напряжение по горизонтали (вызывает сдвиг XZ)

# Расчет напряженности поля (E = U / d)
Ez = Uz / H  
Ex = Ux / L  

# ==========================================================
# 3. ТЕНЗОРНЫЙ РАСЧЕТ (Нотация Фойгта)
# ==========================================================
# Относительные деформации (S = d * E)
S1 = d31 * Ez  # Деформация по X (сжатие при растяжении по Z)
S2 = d31 * Ez  # Деформация по Y
S3 = d33 * Ez  # Деформация по Z (основное удлинение)
S5 = d15 * Ex  # Сдвиговая деформация в плоскости XZ (угол перекоса)

# Абсолютные перемещения (для вывода в консоль)
delta_z = S3 * H
delta_x_shift = S5 * H
delta_x_length = S1 * L

# ==========================================================
# 4. ВИЗУАЛИЗАЦИЯ
# ==========================================================
# Коэффициент масштабирования (чтобы увидеть нано-движения глазом)
scale = 4e4 

def get_actuator_verts(l, w, h, s1, s2, s3, s5, sc):
    """Генерирует вершины параллелепипеда с учетом тензоров"""
    # Новые размеры с учетом масштабированной деформации
    ln = l * (1 + s1 * sc)
    wn = w * (1 + s2 * sc)
    hn = h * (1 + s3 * sc)
    shift_x = s5 * h * sc # Сдвиг верхней грани вдоль оси X
    
    # Координаты 8 вершин [x, y, z]
    return np.array([
        [0, 0, 0], [ln, 0, 0], [ln, wn, 0], [0, wn, 0], # Основание (Z=0)
        [shift_x, 0, hn], [ln+shift_x, 0, hn],          # Верхушка (Z=H)
        [ln+shift_x, wn, hn], [shift_x, wn, hn]
    ])

# Генерация геометрии
v_orig = get_actuator_verts(L, W, H, 0, 0, 0, 0, 0)
v_def  = get_actuator_verts(L, W, H, S1, S2, S3, S5, scale)

# Настройка графики
fig = plt.figure(figsize=(12, 9))
ax = fig.add_subplot(111, projection='3d')

def draw_poly(ax, v, color, label, alpha):
    """Рисует грани объекта"""
    faces = [[v[0], v[1], v[5], v[4]], [v[1], v[2], v[6], v[5]],
             [v[2], v[3], v[7], v[6]], [v[3], v[0], v[4], v[7]],
             [v[0], v[1], v[2], v[3]], [v[4], v[5], v[6], v[7]]]
    poly = Poly3DCollection(faces, facecolors=color, edgecolors=color, alpha=alpha, label=label)
    ax.add_collection3d(poly)

# Рисуем оригинал и деформированный объект
draw_poly(ax, v_orig, 'gray', 'Исходная форма', 0.1)
draw_poly(ax, v_def, 'blue', 'После подачи напряжения', 0.4)

# Оформление осей
ax.set_xlabel('X (Длина), м'); ax.set_ylabel('Y (Ширина), м'); ax.set_zlabel('Z (Высота), м')
ax.set_title(f'Визуализация РГР: Внедрение тензоров в расчёт актуатора\n(Масштаб деформаций: x{scale:.0e})')
ax.set_zlim(0, 0.015)
ax.set_xlim(-0.005, 0.025)

print("-" * 40)
print(f"РЕЗУЛЬТАТЫ РАСЧЕТА (PZT-5H):")
print(f"Вертикальное удлинение (по Z): {delta_z*1e6:.4f} мкм")
print(f"Смещение верхушки (сдвиг XZ):  {delta_x_shift*1e6:.4f} мкм")
print(f"Поперечное сужение (по X):     {delta_x_length*1e6:.4f} мкм")
print(f"Продольная деформация: {S3*1e6:.4f} мкм")
print(f"Сдвиговая деформация:  {S5*1e6:.4f} рад")
print(f"Поперечное деформация: {S1*1e6:.4f} мкм")
print("-" * 40)

plt.show()

# Константы жесткости для PZT-5H (в Паскалях)
C13 = 84.1e9  # Это наши C31 и C32
C33 = 117e9

# Расчет напряжения по вашей тензорной формуле
sigma_z = C13*S1 + C13*S2 + C33*S3

print(f"Внутреннее механическое напряжение sigma_z: {sigma_z / 1e6:.2f} МПа")

# Блокирующая сила (если площадь торца S = L * W)
Area = L * W
F_block = sigma_z * Area
print(f"Блокирующая сила актуатора: {F_block:.2f} Ньютонов")