import numpy as np

# Константы для PZT-5H (в метрах на Вольт)
d31 = -274e-12
d33 = 593e-12
d15 = 741e-12

# Матрица пьезомодулей d (транспонированная для удобства умножения на вектор E)
# Строки: деформации (1-6), Столбцы: поле E (x, y, z)
d_matrix = np.array([
    [0,   0,   d31], # S1 (деформация по x)
    [0,   0,   d31], # S2 (деформация по y)
    [0,   0,   d33], # S3 (деформация по z)
    [0,   d15, 0],   # S4 (сдвиг yz)
    [d15, 0,   0],   # S5 (сдвиг xz)
    [0,   0,   0]    # S6 (сдвиг xy)
])

# Параметры актуатора (в метрах)
L, W, H = 0.02, 0.005, 0.005  # Высота 20мм, ширина/толщина 5мм
U = 500  # Напряжение в Вольтах

# 1. Считаем напряженность поля E3 (по оси Z)
E3 = U / L
E_vector = np.array([0, 0, E3])

# 2. Считаем вектор деформаций S
S = np.dot(d_matrix, E_vector)

print(f"Вектор относительных деформаций S:\n{S}")
